const sum = (a, b) => {
  return a + b;
};

const difference = (c, d) => {
  return c - d;
};

const product = (e, f) => {
  return e * f;
};

const quotient = (g, h) => {
  return g / h;
};

const squareRoot = (x) => Math.sqrt(x);

const maxNum = (j, k, l, n) => Math.max(j, k, l, n);

module.exports = { sum, difference, product, quotient, squareRoot, maxNum };
